char	*ft_strlowcase(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0')
	{
		if (('A' <= str[n] && str[n] <= 'Z'))
		{
			str[n] = str[n] + 32;
		}
		n++;
	}
	return (str);
}
