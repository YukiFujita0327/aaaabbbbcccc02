int		ft_str_is_alpha(char *s)
{
	int n;

	n = 0;
	while (s[n] != '\0')
	{
		if (!(('a' <= s[n] && s[n] <= 'z') || ('A' <= s[n] && s[n] <= 'Z')))
		{
			return (0);
		}
		n++;
	}
	return (1);
}
